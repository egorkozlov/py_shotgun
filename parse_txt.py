def parse_line(line,begin='|',end='|',delim=' '):
    i = line.index(begin) + 1
    iend = line.index(begin,i)
    assert line[i] == delim   
    parsed = list()
    while i < iend:
        if line[i] == ' ':
            i+=1
            continue
        if line[i] == end:
            break
        j = line.index(' ',i)
        parsed.append(line[i:j])
        i = j
    
    return parsed
        
        

